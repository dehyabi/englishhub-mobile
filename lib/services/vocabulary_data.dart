  /// Get words by category
  static List<VocabularyWord> getByCategory(String category) {
    return allWords.where((w) => w.category == category).toList();
  }

  /// Get all unique categories
  static List<String> get categories {
    return allWords.map((w) => w.category).toSet().toList();
  }

  /// Search words by English or Indonesian term
  static List<VocabularyWord> search(String query) {
    final lowerQuery = query.toLowerCase();
    return allWords
        .where((w) =>
            w.english.toLowerCase().contains(lowerQuery) ||
            w.indonesian.toLowerCase().contains(lowerQuery))
        .toList();
  }
